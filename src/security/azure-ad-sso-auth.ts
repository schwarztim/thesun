async function performAzureAdLogin(
  page: Page,
  credentials: { email: string; password: string; mfaScript: string },
  timeout: number
): Promise<{ success: boolean; cookies?: any[]; error?: string }> {
  const startTime = Date.now();

  try {
    // Wait for and fill email field
    console.error("Detecting email field...");
    const emailField = await page.waitForSelector(
      'input[type="email"], input[name="loginfmt"], input[name="login"], input[name="email"]',
      { timeout: 30000 }
    );

    await emailField.fill(credentials.email);
    console.error("Email field filled");

    // Click next/submit for email
    const nextButton = await page.waitForSelector(
      'input[type="submit"], button[type="submit"], #idSIButton9',
      { timeout: 10000 }
    );
    await nextButton.click();
    console.error("Email submitted");

    // Wait for password field
    await page.waitForTimeout(1500);
    console.error("Detecting password field...");
    const passwordField = await page.waitForSelector(
      'input[type="password"], input[name="passwd"], input[name="password"]',
      { timeout: 30000 }
    );

    await passwordField.fill(credentials.password);
    console.error("Password field filled");

    // Click sign in
    const signInButton = await page.waitForSelector(
      'input[type="submit"], button[type="submit"], #idSIButton9',
      { timeout: 10000 }
    );
    await signInButton.click();
    console.error("Sign in clicked");

    // Handle MFA if present
    await page.waitForTimeout(1000);
    const mfaField = await page.waitForSelector(
      'input[name="otc"], input[name="totp"], input[aria-label*="code"], #idTxtBx_SAOTCC_OTC',
      { timeout: 20000 }
    ).catch(() => null);

    if (mfaField && credentials.mfaScript) {
      console.error("MFA field detected, executing script...");
      try {
        // Execute MFA script safely using execFileSync
        const mfaCode = execFileSync(credentials.mfaScript, [], { encoding: "utf-8" }).trim();
        await mfaField.fill(mfaCode);

        const verifyButton = await page.waitForSelector(
          'input[type="submit"], button[type="submit"], #idSubmit_SAOTCC_Continue',
          { timeout: 5000 }
        );
        await verifyButton.click();
        console.error("MFA code submitted");
      } catch (error: any) {
        console.error("MFA script failed:", error.message);
      }
    } else if (mfaField && !credentials.mfaScript) {
      console.error("MFA field detected but no script configured - waiting for manual entry...");
    }

    // Handle "Stay signed in?" prompt
    await page.waitForTimeout(1000);
    const staySignedIn = await page.waitForSelector(
      '#idBtn_Back, #idSIButton9, button:has-text("Yes"), button:has-text("No")',
      { timeout: 10000 }
    ).catch(() => null);

    if (staySignedIn) {
      const yesButton = await page.$('#idSIButton9, button:has-text("Yes")');
      if (yesButton) {
        await yesButton.click();
        console.error("'Stay signed in?' accepted");
      }
    }

    // Wait for redirect to success URL
    console.error("Waiting for redirect...");
    await page.waitForURL(SUCCESS_URL_PATTERN, { timeout: timeout - (Date.now() - startTime) });
    console.error("Redirected to target service");

    // Get cookies
    const context = page.context();
    const cookies = await context.cookies();

    return { success: true, cookies };
  } catch (error: any) {
    return { success: false, error: error.message };
  }
}

