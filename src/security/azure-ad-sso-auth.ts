/**
 * Azure AD SSO Authentication Module for thesun
 *
 * Enterprise-grade browser-based SSO authentication for Microsoft/Azure AD protected services.
 * This module enables generated MCPs to authenticate to enterprise applications that use
 * Azure AD, Entra ID, or Microsoft SSO.
 *
 * Features:
 * - Headless-first authentication (background, no visible browser)
 * - Automatic fallback to visible browser if headless fails
 * - Automatic 401 detection and re-authentication
 * - Credential storage in system keychain (secure)
 * - Cookie caching with expiration detection
 * - MFA/TOTP script support (optional)
 * - Proper browser cleanup and timeouts
 *
 * Based on the battle-tested ServiceNow MCP authentication system.
 */

import { z } from "zod";

