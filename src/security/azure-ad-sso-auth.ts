/**
 * Generate the setup wizard (CLI script)
 */
export function generateSetupWizardModule(config: AzureAdSsoConfig): string {
  const { toolName } = config;
  const envPrefix = toolName.toUpperCase().replace(/-/g, "_");

  return `#!/usr/bin/env node
/**
 * ${toolName} MCP Setup Wizard
 *
 * Interactive setup for Azure AD SSO authentication.
 * Stores credentials securely in system keychain.
 *
 * Generated by thesun.
 */

import prompts from "prompts";
import { existsSync, mkdirSync, writeFileSync } from "node:fs";
import { homedir } from "node:os";
import { join } from "node:path";

const CONFIG_DIR = join(homedir(), ".${toolName}-mcp");
const CONFIG_FILE = join(CONFIG_DIR, "config.json");

async function getKeytar() {
  try {
    const keytar = await import("keytar");
    return keytar.default;
  } catch {
    console.error("⚠️  keytar not available - passwords will not be stored securely");
    return null;
  }
}

async function main() {
  console.log("\\n🔧 ${toolName} MCP Setup Wizard\\n");
  console.log("This will configure Azure AD SSO authentication.\\n");

  const { instanceUrl } = await prompts({
    type: "text",
    name: "instanceUrl",
    message: "${toolName} URL:",
    initial: process.env.${envPrefix}_URL || "",
    validate: (v) => v.startsWith("https://") || "Must be an HTTPS URL",
  });

  if (!instanceUrl) {
    console.log("Setup cancelled.");
    process.exit(1);
  }

  const { email } = await prompts({
    type: "text",
    name: "email",
    message: "Your email address:",
    validate: (v) => v.includes("@") || "Invalid email",
  });

  if (!email) {
    console.log("Setup cancelled.");
    process.exit(1);
  }

  const { password } = await prompts({
    type: "password",
    name: "password",
    message: "Your password:",
    validate: (v) => v.length > 0 || "Password required",
  });

  if (!password) {
    console.log("Setup cancelled.");
    process.exit(1);
  }

  const { mfaScript } = await prompts({
    type: "text",
    name: "mfaScript",
    message: "Path to MFA/TOTP script (leave empty to skip):",
    initial: "",
    validate: (v) => {
      if (!v) return true;
      if (!existsSync(v)) return \`File not found: \${v}\`;
      return true;
    },
  });

  if (!existsSync(CONFIG_DIR)) {
    mkdirSync(CONFIG_DIR, { recursive: true });
  }

  const config = {
    instanceUrl: instanceUrl.replace(/\\/$/, ""),
    email,
    mfaScript: mfaScript || "",
    headless: true,
  };

  writeFileSync(CONFIG_FILE, JSON.stringify(config, null, 2));
  console.log(\`\\n✅ Config saved to \${CONFIG_FILE}\`);

  const keytar = await getKeytar();
  if (keytar) {
    await keytar.setPassword("${toolName}-mcp", email, password);
    console.log("✅ Password stored securely in system keychain");
  } else {
    console.log("⚠️  Password NOT stored - keytar not available");
  }

  console.log("\\n🎉 Setup complete!\\n");
}

main().catch(console.error);
`;
}

