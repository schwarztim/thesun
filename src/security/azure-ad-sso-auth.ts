export const AzureAdSsoConfigSchema = z.object({
  toolName: z
    .string()
    .describe("Name of the tool/service (e.g., 'servicenow')"),
  loginUrl: z.string().url().describe("URL that triggers Azure AD login"),
  successUrlPattern: z
    .string()
    .describe("Regex pattern for successful auth redirect"),
  headlessTimeout: z
    .number()
    .default(60000)
    .describe("Timeout for headless auth (ms)"),
  visibleTimeout: z
    .number()
    .default(300000)
    .describe("Timeout for visible browser auth (ms)"),
  cookieMaxAge: z
    .number()
    .default(8 * 60 * 60 * 1000)
    .describe("Cookie cache max age (ms)"),
  enableMfa: z
    .boolean()
    .default(false)
    .describe("Whether MFA/TOTP is expected"),
});

export type AzureAdSsoConfig = z.infer<typeof AzureAdSsoConfigSchema>;

